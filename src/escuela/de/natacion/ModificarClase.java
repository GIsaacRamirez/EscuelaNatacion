    public String nombreInstructor(String idClase)
    {
        String nombre="";
        try
            {
                
                ConexionMySQL conexion = new ConexionMySQL();
                conexion.MySQLConnection();
                Statement st = Conexion.createStatement();
                ResultSet rs = st.executeQuery("SELECT Nombre,ApellidoPaterno,ApellidoMaterno FROM empleado INNER JOIN clase ON clase.IdInstructor=empleado.IdEmpleado WHERE IdClase="+idClase+"");
                while(rs.next())
                {
                   nombre=rs.getString("Nombre")+" "+rs.getString("ApellidoPaterno")+" "+rs.getString("ApellidoMaterno");
                }
                rs.close();
                conexion.closeConnection();
                
            }
            catch (SQLException ex)
            {
                JOptionPane.showMessageDialog(rootPane, ex);
            }
        return nombre;
    }
    public void muestraInstructores()
   {
       try
            {
                ConexionMySQL conexion = new ConexionMySQL();
                conexion.MySQLConnection();
                Statement st = Conexion.createStatement();
                ResultSet rs = st.executeQuery("SELECT IdEmpleado, Nombre FROM empleado WHERE Instructor=true");
                while(rs.next())
                {
                    int id= rs.getInt("IdEmpleado");
                    String nombre=rs.getString("Nombre");
                    jComboBox3.addItem(Integer.toString(id)+","+nombre);
                }
                rs.close();
                conexion.closeConnection();
                
            }
            catch (SQLException ex)
            {
                JOptionPane.showMessageDialog(rootPane, ex);
            }
   }
