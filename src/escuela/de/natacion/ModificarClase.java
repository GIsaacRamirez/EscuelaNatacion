    public String ObtenIdInstructor(String IdClase){
        String IdInstructor="";
          try
            {
                
                ConexionMySQL conexion = new ConexionMySQL();
                conexion.MySQLConnection();
                Statement st = Conexion.createStatement();
                ResultSet rs = st.executeQuery("SELECT empleado.IdEmpleado FROM empleado INNER JOIN clase ON clase.IdInstructor=empleado.IdEmpleado WHERE IdClase="+IdClase+"");
                while(rs.next())
                {
                  IdInstructor=rs.getString("IdEmpleado");
                }
                rs.close();
                conexion.closeConnection();
                
            }
            catch (SQLException ex)
            {
                JOptionPane.showMessageDialog(rootPane, ex);
            }
        return IdInstructor;
    }
    
