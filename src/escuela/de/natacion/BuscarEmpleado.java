    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!txtBuscarIdEmpleado.getText().matches("^\\s*$"))
        {
            try 
            {
                ConexionMySQL conexion = new ConexionMySQL();
                conexion.MySQLConnection();
                Statement st = Conexion.createStatement();
                ResultSet rs = st.executeQuery("Select IdEmpleado from empleado where IdEmpleado="+Integer.parseInt(txtBuscarIdEmpleado.getText()));
                while(rs.next())
                {
                    int id= rs.getInt("IdEmpleado");
                    rs.close();
                    conexion.closeConnection();
                    Empleado BuscEmpleado=new Empleado(id,Opcion);
                    BuscEmpleado.setVisible(true);
                    this.dispose();
                }
                JOptionPane.showMessageDialog(rootPane,"No Encontrado");
            }
            catch (SQLException ex) 
            {
                
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

